async def predict_file(file: UploadFile = File(..., max_size=100_000_000)):
    try:
        predictions = []
        predictions_completes = []
        nombre_predictions_completes=0
        # Lecture directe du fichier upload
        csv_file = TextIOWrapper(file.file, encoding='utf-8')
        csv_reader = csv.reader(csv_file)
        
        # Ignorer l'en-tte
        next(csv_reader, None)
        for row in csv_reader:
            converted_row = []
            for value in row:
                try:
                    converted_value = np.float64(value)
                except ValueError:
                    converted_value = value
                converted_row.append(converted_value)
            converted_row.append(predict(converted_row))
            # dictionnaire = dict(zip(noms_colonnes, converted_row))
            dictionnaire = dict(zip(["Port", "Prediction"], [converted_row[0],converted_row[-1]]))
            predictions.append(converted_row[-1])
            if nombre_predictions_completes<5:
              predictions_completes.append(dictionnaire)
              nombre_predictions_completes+=1
            compteur = {}
            for valeur in predictions:
                if valeur in compteur:
                    compteur[valeur] += 1
                else:
                    compteur[valeur] = 1


        nbr_ddos=compteur.get("DDoS", 0)
        nbr_benigh=compteur.get("BENIGN", 0)
        
        return {
            "summary":{
               "total_connections": nbr_ddos+nbr_benigh,
                "ddos_attacks":nbr_ddos,
                "benign_connections":nbr_benigh,
                "ddos_percentage": 100*nbr_ddos/(nbr_benigh+nbr_ddos),
                "benign_percentage": 100*nbr_benigh/(nbr_benigh+nbr_ddos)
            },
            "predictions": predictions_completes
        }
        
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Erreur de traitement: {str(e)}")
    finally:
        file.file.close()
